export const formatDate = (dateStr) => {
  //FIXING ISSUE NOT IN KANBAN: bills with empty dates would make the view break and prevent the bill category to be displayed in admin view"
  //Added if statement so that if the date equals '', the raw value is returned by formatDate instead of breaking
  if (dateStr != "") {
  const date = new Date(dateStr)
  const ye = new Intl.DateTimeFormat('fr', { year: 'numeric' }).format(date)
  const mo = new Intl.DateTimeFormat('fr', { month: 'short' }).format(date)
  const da = new Intl.DateTimeFormat('fr', { day: '2-digit' }).format(date)
  const month = mo.charAt(0).toUpperCase() + mo.slice(1)
  return `${parseInt(da)} ${month.substr(0,3)}. ${ye.toString().substr(2,4)}`
  } else {
    return dateStr
  }
}
 
